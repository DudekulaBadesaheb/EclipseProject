package oops.wrapperClasses;

public class WrapperClassesDemo {
	

	public static void main(String[] args) {
		String stringNumber = "10";
		Integer number = Integer.parseInt(stringNumber);
		System.out.println(number);

		Byte byteNumber = 127;
		System.out.println(byteNumber);

		Short shortNumber = 12355;
		System.out.println(shortNumber);

		Long longNumber = 1234567l;
		System.out.println(longNumber);

		Double doubleNumber = 1234.2;
		System.out.println(doubleNumber);

		Float floatNumber = 1234.1f;
		System.out.println(floatNumber);

		Integer intNumber = 123456;
		System.out.println(intNumber);

		Boolean booleanNumber = true;
		System.out.println(booleanNumber);
		
		Character charNumber='3';
		System.out.println(charNumber);

	}

}
