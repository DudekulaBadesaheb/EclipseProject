package basics.ch_11_loops.while_loop;

public class Whileloop {

	public static void main(String[] args) {
		// dry principle
		// do not repeat
		// print numbers from 1 to 5
		// loops
		// 1. when to start
		// 2. code to run
		// 3. when to stop
		int counter =0;
		while (counter<6) {
		System.out.println(counter);
		// counter=counter=1// 1 2 3 4 5 
		counter++;// 1 2 3 4 5 
		
		}
		
			
		
	
	}

}

