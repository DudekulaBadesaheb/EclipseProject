package oops.encapsulation.task2;

public class Product {
	private String name;
	private String catagiriy;
	private int rating;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCatagiriy() {
		return catagiriy;
	}

	public void setCatagiriy(String catagiriy) {
		this.catagiriy = catagiriy;
	}

	public int getRating() {
		return rating;
	}

	public void setRating(int rating) {
		this.rating = rating;
	}

}
